MeshComponent::MeshComponent(const char* path, bool FlipUvs) : BaseComponent()
{
	loadModel(path, FlipUvs);
}

void MeshComponent::Draw()
{
	for (unsigned int i = 0; i < meshes.size(); i++)
		meshes[i].Draw();
}

